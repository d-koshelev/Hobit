import type {
  AssetPackManifest,
  SpriteSheetAssetDefinition,
} from "../contracts";
import { generatedFrameSize, spriteSheetSources } from "../sampleSprites";

const previewFrame = generatedFrameSize;

function publicSpriteSheet(
  key: string,
  frameWidth: number,
  frameHeight: number,
  frameCount: number,
  description: string,
): SpriteSheetAssetDefinition {
  return {
    key,
    kind: "spriteSheet",
    src: `spritesheets/${key}.png`,
    frameWidth,
    frameHeight,
    frameCount,
    columns: frameCount,
    rows: 1,
    description,
  };
}

function fallbackSpriteSheet(
  key: string,
  src: string,
  frameCount: number,
  description: string,
): SpriteSheetAssetDefinition {
  return {
    key,
    kind: "spriteSheet",
    src,
    frameWidth: previewFrame.width,
    frameHeight: previewFrame.height,
    frameCount,
    columns: frameCount,
    rows: 1,
    description,
  };
}

